void oled_write_modified_font(const char* str) {
    char top_line[32] = {0};
    char bot_line[32] = {0};

    uint8_t i = 0;
    while (str[i] && i < sizeof(top_line) - 1) {
        uint8_t ascii_val = (uint8_t)str[i];
        uint8_t result = ascii_val / 0x20;
        top_line[i] = ascii_val + (result * 0x20);
        bot_line[i] = ascii_val + ((1 + result) * 0x20);
        i++;
    }

    oled_write(top_line, false);
    oled_write(bot_line, false);
}

void render_layer_state(void){
    switch (get_highest_layer(default_layer_state)) {
        case 0: oled_write_modified_font(" GRAPHITE "); break;
        case 1: oled_write_modified_font("  QWERTY  ");
    }

    oled_set_cursor(0, 2);
    switch (get_highest_layer(layer_state)) {
        case 3: oled_write_modified_font("    SYM   "); break;
        case 4: oled_write_modified_font("   F7-12  "); break;
        case 2: oled_write_modified_font("    \x1B\x19\x1A   "); break;
        default: oled_write_modified_font("----------");
    }
}

